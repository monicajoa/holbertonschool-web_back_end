#!/usr/bin/env python3
"""[Basic annotations - Concat]
"""


def concat(str1: str, str2: str) -> str:
    """[Type-annotated function concat that takes str1 and str2 as arguments]

    Args:
        str1 (str): [First string]
        str2 (str): [Second string]

    Returns:
        str: [Concatenated string]
    """
    return str1 + str2
